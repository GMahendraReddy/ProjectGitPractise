package Conditionalstatements;

public class IfelseIf {

	public static void main(String[] args) {
		int a = 20;
		if(a>88) {
			System.out.println("A");
		}
		else if(a>60) {
			System.out.println("B");
			
		}
		else if(a>40) {
			System.out.println("C");
			
		}
		else if(a>30) {
			System.out.println("D");
			
		}
		else {
			System.out.println("Fail");
		}

	}

}
